#!/bin/sh
# map ascii-art'd drawings to utf-8 fancy chars, eg:
#    ###A###     ###B###
#    .-----.     .-----.
# ->-|     |-----|     |-.-->
#    '--.--'     '--.--' |
#       |           '-<--'
#  .----'----.
#  |         |......<>
#  '-=======-'
sed "
s/-'-/─┴─/g;
s/-[.]-/─┬─/g;
s/[.]-/┌─/g;
s/-[.]/─┐/g;
s/'-/└─/g;
s/-'/─┘/g;
s/'=/┕━/g;
s/='/━┙/g;
s/|=/┝━/g;
s/=|/━┥/g;
s/-|/─┤/g;
s/|-/├─/g;
s/<-/←─/g;
s/->/─→/g;
s/<>/←→/g;
s/+/┼/g;
s/#/▒/g;
s/[.][.]/┈┈/g;
s,/,╱,g;
s,[\],╲,g;
s/|/│/g;
s/X/╳/g;
s/=/━/g;
s/-/─/g;
" "${@}"
